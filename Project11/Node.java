public class Node {
	// instance variables
	private int x;
	private int y;
	// constructor method 
	public Node (int i, int j )
	{
		this.x=i;
		this.y=j;	
	}
	// getter method returns value of x	
	public int getX() {
		return x;
	}
// getter method returns value of y
	public int getY() {
		return y;
	}
}
